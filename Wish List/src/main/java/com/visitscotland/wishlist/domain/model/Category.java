package com.visitscotland.wishlist.domain.model;

/**
 * Enumeration representing the classification of wish list items.
 * 
 * Categories are used to group and filter items in the wish list,
 * enabling semantic organization and targeted retrieval.
 *
 * @author John Paul Brogan
 * @date 2025-09-10
 */
public enum Category
{
    /** Represents lodging or overnight stay options. */
    ACCOMMODATION,

    /** Represents tourist attractions or points of interest. */
    ATTRACTION,

    /** Represents scheduled events such as festivals or performances. */
    EVENT
}
